package com.insideview;

public class DataRecord {

	short jobLevel;
	int jobFunction;
	int empCount;
	int popularity;
	double revenue;
	boolean fortuneListed;
	String email;
	boolean label;
	int compId;
	int execId;
	int empId;

	public short getJobLevel() {
		return jobLevel;
	}

	public void setJobLevel(short jobLevel) {
		this.jobLevel = jobLevel;
	}

	public int getJobFunction() {
		return jobFunction;
	}

	public void setJobFunction(int jobFunction) {
		this.jobFunction = jobFunction;
	}

	public int getEmpCount() {
		return empCount;
	}

	public void setEmpCount(int empCount) {
		this.empCount = empCount;
	}

	public int getPopularity() {
		return popularity;
	}

	public void setPopularity(int popularity) {
		this.popularity = popularity;
	}

	public double getRevenue() {
		return revenue;
	}

	public void setRevenue(double revenue) {
		this.revenue = revenue;
	}

	public boolean isFortuneListed() {
		return fortuneListed;
	}

	public void setFortuneListed(boolean fortuneListed) {
		this.fortuneListed = fortuneListed;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public boolean isLabel() {
		return label;
	}

	public void setLabel(boolean label) {
		this.label = label;
	}

	public int getCompId() {
		return compId;
	}

	public void setCompId(int compId) {
		this.compId = compId;
	}

	public int getExecId() {
		return execId;
	}

	public void setExecId(int execId) {
		this.execId = execId;
	}

	public int getEmpId() {
		return empId;
	}

	public void setEmpId(int empId) {
		this.empId = empId;
	}

}
